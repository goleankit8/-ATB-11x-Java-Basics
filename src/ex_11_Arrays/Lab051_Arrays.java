package ex_11_Arrays;

public class Lab051_Arrays {
    public static void main(String[] args) {
        String[] names={"Ankit", "Shubham", "Rajesh", "Ravi"};
        System.out.println(names.length);
        System.out.println(names[0]);
        System.out.println(names[1]);
        System.out.println(names[2]);
        System.out.println(names[3]);
        System.out.println("----------------------------------------");

        String[] name_11x=new String[3];
        name_11x[0]="Gole";
        name_11x[1]="Shubham";
        name_11x[2]="Rajesh";
        System.out.println(name_11x.length);
        System.out.println(name_11x[0]);
        System.out.println(name_11x[1]);
        System.out.println(name_11x[2]);

    }
}
