package ex_04_Increatement_Decrement_Operator;

public class Lab024_incre_decr {
    public static void main(String[] args) {
        int a=10;
        System.out.println(++a);

        //post increament
        int a_post=10;
        System.out.println(a_post++);
        System.out.println(a_post);
    }
}
